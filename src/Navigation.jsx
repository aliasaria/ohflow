const data = [
  { link: "", label: "Workflows", icon: IconSitemap },
  { link: "", label: "Workflow Triggers", icon: IconArrowMoveRight },
  { link: "", label: "Workflow Templates", icon: IconBooks },
  { link: "", label: "Node Library", icon: IconCategory2 },
  { link: "", label: "Logs", icon: IconList },
  { link: "", label: "Debug", icon: IconBug },
];

export default function NavbarSimple({ active, onChange }) {
  const { classes, cx } = useStyles();

  const links = data.map((item) => (
    <a
      className={cx(classes.link, {
        [classes.linkActive]: item.label === active,
      })}
      href={item.link}
      key={item.label}
      onClick={(event) => {
        event.preventDefault();
        onChange(item.label);
      }}
    >
      <item.icon className={classes.linkIcon} stroke={1.5} />
      <span>{item.label}</span>
    </a>
  ));

  return (
    <Navbar style={{ width: 300 }} width={{ sm: 300 }} p="sm">
      <Navbar.Section grow>
        <Group className={classes.header} position="apart">
          {/* <MantineLogo size={28} /> */}
          <IconArrowsShuffle />
          OhFlow
          <Code sx={{ fontWeight: 700 }}>v0.1.3</Code>
        </Group>
        {links}
      </Navbar.Section>

      <Navbar.Section className={classes.footer}>
        <a
          href="#"
          className={classes.link}
          onClick={(event) => event.preventDefault()}
        >
          <IconSwitchHorizontal className={classes.linkIcon} stroke={1.5} />
          <span>Change account</span>
        </a>

        <a
          href="#"
          className={classes.link}
          onClick={(event) => event.preventDefault()}
        >
          <IconLogout className={classes.linkIcon} stroke={1.5} />
          <span>Logout</span>
        </a>
      </Navbar.Section>
    </Navbar>
  );
}
